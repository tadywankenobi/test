		<p>I have been involved with Pegasus (established by Swiss-born Tina Schmill) since 2005 as one of three Directors. As a not-for-profit organisation with charitable status, it grew from only a few riders to more than 95 clients in 2008.</p>

		<p>After completing TATA in 2004 and thanks to generous sponsorship, I was able to donate 30,000 euros in installments to Pegasus over a four-year period. I was also in a position to help with numerous other aspects of the centre.</p>
		
		<p>There were many challenges though, including having to find new premises for 2009 and difficulties in having enough funds to operate on an annual basis, including paying a salary to two qualified therapeutic riding instructors.</p>
		
		<p>The lack of an indoor arena and the unpredictable weather in the northwest did not help matters either, resulting in hundreds of cancellations every year.</p>
		
		<p>The recession in Ireland combined with the above problems forced us to make a decision in November 2008 to regrettably close down. This course of action was supported by Jill Carey, CEO of the Festina Lente Foundation in Bray Wicklow, who has been acting as advisor to Pegasus.</p>
		
		<p>It is always sad to see something that required so much time and energy coming to an end, however, the impact that Pegasus has had on so many clients, their families, volunteers and everyone involved, has been substantial and the good memories will be there for a long time to come.</p>
		
		
		
		<p>Petra who worked as a therapeutic riding instructor at Pegasus decided to go back to Slovakia and walked with her horse Momo (donated to her by Pegasus) from Germany to her home country. She started her adventure in June 2009 and finished three months later. </p>
		
