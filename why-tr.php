		<p>Therapeutic riding is an umbrella term covering several distinct methods of equine therapy including therapeutic sport riding, hyppotherapy and vaulting.</p>

		<p>Equine Therapy offers benefit to a wide range of physical issues. For example, individuals who have spasticity (an involuntary muscle reaction), riding a horse in a therapeutic situation has been shown to reduce the affliction. Many individuals along the Autistic Spectrum Disorder (ASD) experience significant improvements in sensory integration as a result of the dynamic movement.</p>

		<p>Equine Therapy and its benefits have been recognized for many years. It is a proven method of addressing the physical, mental, emotional and developmental capabilities of individuals with disabilities, functional limitation or medical issues. There is a healing interaction between horses, riders and the people who help them.</p>
		
