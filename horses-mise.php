		<h3>Mise</h3>
		
		<p>I remember the day very well when I met Mise (pronounced “Misha”, meaning “me” in Irish). It was a few weeks after TATA formally commenced and I was struggling with just one horse. Through a combination of relentless enthusiasm by Florencia (a newly made friend) urging me to to travel north immediately, and my frustration at not having yet found the right horse to complete the team, I ended up in Cordoba sooner than planned.</p>
		
		<img src="images/mise1.jpg" alt="Mise with Marianne" title="Mise with Marianne" class="box left"/>
		
		<p>Things were not that straightforward though and after a number of introductions to so-called <em>Criollos</em> (the breed I was looking for) – many with badly damaged hooves, cut tails or simply bad attitude, it initially looked as if I wasn’t in for much luck. But that all changed when I was told about a Criollo at the polo club where Tusa resided. I still have a vivid recollection the morning I was supposed to meet her;   feelings of nervousness and excited anticipation while saying a silent prayer that she would be “the one”.</p>
		
		<p>Mise was already tacked out in traditional gaucho saddle and reins when I got to the polo club. She moved around restlessly and gave an impatient snort, almost a bit indignant that she had to be scrutinised in such a way. I could see her eyes through a long black fringe.  I was in no doubt that this was one special mare with ample spirit. </p>

		<p>I took her for a ride and an exaggerated trying-to-impress walk, made her head sway comically from side to side. She even did a little bucking at the start but that I learned later, was only because the girth around her tummy was too tight. The moment I actually <em>saw</em> Mise I knew that my search had come to an end – the ‘test drive’ was purely academic. </p>
		
		<p>Mise, was the only mare I used during TATA. She proved to be excellent throughout the trip although not without the occasional female moodiness and wanting things her way. I preferred riding her and so did she, never appearing to be completely comfortable carrying the pack. Somehow it was as if she felt it was beneath her.</p>
		
		<img src="images/mise2.jpg" width="760" alt="Mise" title="Mise" class="box"/>
		
		<p>Mise often behaved like a watchdog and would notice and hear things long before I did. I would know immediately that there was something around when she would break into a lively energetic gait with ears erect. Even if she had an ache somewhere, that would be temporarily forgotten when she noticed movement or figures in the distance.</p>

		<img src="images/mise3.jpg" alt="Mise with Tusa" title="Mise with Tusa" class="box left"/>
		
		<p>She was very attached to Tusa even though she had no qualms pushing him out of the way sometimes to get to the food first or giving him little bites. One of these proved to be not so little when in Salta Argentina she imposed such a vicious bite on Tusa’s back that it delayed our travelling for at least two weeks.</p>
		
		<p>Whoever meets Mise, falls in love with her. She just has this special way about her, not overly needy and dependent on people, yet always trying to please and behave in a proper way. </p>
		
		<p><em>Mise has been living in Ireland since May 2004.</em></p>
		
