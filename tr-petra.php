		<h2 class="title">Petra</h2>
	
		<p>"Momo (who used to be part of the Pegasus Therapeutic Riding Program) and I started our BIG TRIP in Sligo at the beginning of May 2009, heading for Dublin.</p>

<p>I had no idea where I was going to stay every night and I will never forget the first few steps - like stepping outside the world into a dream. The dream became close to a nightmare when on the first evening and with rain pouring down, I had no place to sleep. But after rain comes the rainbow and I quickly found out that there is always a place for a traveler in Ireland. </p>

<p>The trip to Dublin took us a week. It was wet every day (with occasional sun) and we found a good place to sleep every night. </p>

<p>Traveling through Ireland is so easy!</p>

<p>In a few places people even wanted to buy Momo and at one place they offered me a job. Our confidence grew with each passing day and the world opened up. It was just great.</p>

<p>The first big disappointment came in Dublin when the transport company that was supposed to bring us from Dublin to Geldern
at the German-Netherland borders, simply did not show up. If it wasn’t for Marianne with a solution for everything, I have no idea what I would have done, standing with the pony in Dublin Port on a Friday evening – no ship to leave on and no place to go to.</p>

<p>Marianne became our guardian angel (once again) and took us home...well, only me, because there was not enough place for Momo on her balcony. So we slept outside, on a perfectly manicured lawn in front of her apartment in the city. </p>

<p>Here, I must say that the Irish people turned out to be incredibly tolerant. A lady from the house came to me in the morning, handed Momo a few cookies and with a smile said: "I knew I smelled a horse out here last night!" </p>

<p>Before the end of the day, Marianne managed to find a place for Momo at the DSPCA and we started organising a plan again to get across the Irish Sea. After many calls and a few ‘subtle threats’ to the company that deserted me in the first place, Momo and I were set to start our adventure!</p>

<p>I’ve had promises of assistance from the Rotary Club to help us with accommodation and contacts as we walked through Germany and the Czech Republic, but this did not happen immediately. The first clubs on the way refused to help us – either they could not be bothered or they thought that I was crazy.</p>

<p>So for the first 200 kilometers I was on my own in the largest industrial zone in Germany. I was afraid that the Germans were not going to be as welcoming and helpful as the Irish but I needn’t have worried about anything. They were even more forthcoming. People on the streets would offer me food, money or water for Momo. I didn't need to ask for anything.</p>

<p>One day a lady stopped me on the street wanting to chat for a short while. Next thing she invited me to her home for tea. Then lunch followed. It was Friday so she suggested I stayed for the weekend. I did not leave for another week.</p>

<p>This is only one of many stories about the German hospitality that I received.</p>

<p>After two weeks I finally met some people from the Rotary Club. They
realised by now that I was serious about my journey and offered lots of help. </p>

<p>From there on, my sojourn through Germany was like one long extended holiday. Individuals from the Rotary Club created a chain of assistance in the places I was going through. I met some incredible people, was introduced to all the beauty spots and will always remember places like Marburg, Lauterbach and Fichtelgebirge to name just a few. </p>

<p>At the end of July I was ready to cross the border into the Czech Republic. It was like the beginning again - no contacts, nobody to help me, no maps. After those happy weeks in Germany it was a little bit of a culture shock. </p>

<p>It was especially challenging because I had to go through a really rough part of the Czech Republic called the Bohemian Forest, which used to be a “border area” during the cold war and therefore no-man’s-land. I found abandoned villages with houses falling apart and many poor people. Luckily enough, people were friendly and nice despite their thinking I was not in my right mind.</p>

<p>After a few days I realised that I could make this as easy as the German or Irish stretch and I really started enjoying it. I met many interesting people, found many open doors and made new friends. I was invited to visit several castles and museums, took part in a wedding and also saw the famous Moravian wine cellars. </p>

<p>When the Slovak borders came close I could not quite believe it. I was happy and sad at the same time. I knew that the adventure would be over soon and I just didn’t want it to end. The Slovak part was short – only five days and except for two days when I slept at my friends’ place, I had to sleep outside in a tent. </p>

<p>I got totally lost in a big forest and didn’t manage to find any other place to stay. Momo was used to sleeping outside and had no problem with it.</p>

<p>And then, more than three months later, on the 9th of September 2009, my trip was over. Momo came to his new home and I was ready to start a new life.</p>

<p>What can I say?</p>

<p>It was the best thing that I could have done. It changed my life and my priorities. I was lucky not to have had any bad experiences and Momo was an excellent companion, always ready for everything. He never stopped or pulled back. Sometimes I think that it was he that made this trip and he just took me along.</p>

<p>Now we have a successful therapeutic riding program and along with Momo we have another horse, a Haflinger called Sunny.</p>

<p>I would like to thank Marianne for the inspiration and great help she gave me. Without her energy and advice the trip would not have been possible."</p>

<p>PETRA JANECKOVA<br />
October 2010</p>
		
