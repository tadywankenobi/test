		<h3>Tu Fein</h3>
		
		<p>Tu Fein joined the TATA trio in Manaus Brazil when Tusa sadly had moved on to a more peaceful place. I was told that Tu Fein was part Criollo but I think that was only said because they knew I was looking for one. 
		<img src="images/tufein1.jpg" alt="Tu Fein with Marianne" title="Tu Fein with Marianne" class="box left" />
		He was quarter horse maybe with some other mixture but that was it. He would not have been considered the most handsome of horses but to me he was lovely. His facial expression always made him seem to be on the verge of breaking in to tears.</p>
		
		<p>Tu Fein must have had a difficult upbringing because during our first encounter I noticed his nervousness and over-sensitive demeanour. In fact, this was the reason I decided to take him, hoping that with love and care he will get less jumpy and paranoid and more trusting.</p>
		
		<p>So suspicious was he that initially he would not even take a carrot or other food from my hand. He would view the piece of food with great concern, rolling his eyes with the white parts showing, before grabbing the grub like a dog. But it was not long before this behaviour changed and I felt great pride when it became obvious that he had more faith in me, appearing calm in my presence.</p>
		
		<p>He got on well with Mise and accepted her as the leader. I think he secretly fancied her and always seemed particularly envious when other horses showed her their appreciation. Tu Fein was loyal, hard working and always ready to serve. He did not demand anything in return, only continued love and affection, which I gave him in abundance. He never received admiration from people in the way that Mise did so I always made sure that he received extra attention from me.</p>
		
		<img src="images/tufein2.jpg" alt="Tu Fein with Mise and Marianne" title="Tu Fein with Mise and Marianne" width="760px" class="box" />
		
		<p>Going through the Guatemalan/Mexican mountains over very rugged terrain and through rivers was not easy but Tu Fein proved that he was up for anything and just took it all in his stride, showing tremendous stability and stamina. </p>
		
		
		
		<p>I was devastated when he was diagnosed with Piroplasmosis once we were back in Guatemala again. I made contact with numerous veterinarians 
		<img src="images/tufein3.jpg" alt="Tu Fein with Marianne" title="Tu Fein with Marianne" class="box left" />
		in different parts of the world and received the same advise: if he has both forms of the disease (which he did), treatment would be nearly impossible and could in fact cause his death and much suffering. Nobody in Guatemala was going to keep Tu Fein in isolation for the rest of his life and that would not have been a fair life for him anyway. Taking him with me was impossible because he could not get the necessary papers because of his disease.</p>

		<p>A heartbreaking decision was made to put Tu Fein down. I had no other option.</p>
		
		<p>He was a true star. </p>
		
		<p>May he rest in peace.</p>
		
