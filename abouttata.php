		<p>I trotted through Central Park towards Times Square with two of my loyal and faithful equine companions amidst many stares and friendly smiles from curious New Yorkers. At the far end waited my mom (a surprise!), friends and loved ones from Ireland.
		
		<p>With me was Toto, a Tennessee Walking-Horse with attitude and big dreamy eyes who had been part of TATA for the stretch through the United States. Mise, my beloved mare who shared in so many of the joys and tribulations since the beginning of this trip, was on the leading rope.
Basking in the warm winter sun and trotting through the tranquil park, I fondly remembered Tusa and Tu Fein, who sadly had gone to a more peaceful place. </p>

