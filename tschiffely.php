			<p>The challenge of plain, jungle, desert and mountain had been calling to Swiss schoolteacher Aimé Tschiffely for many years, and in 1925 he set off with his two horses from Buenos Aires in Argentina to New York, a journey of more than 10,000 miles, covering eleven countries en route. His companions for the trail were two Argentinean Criollo geldings: Mancha, a sixteen-year-old red and white overo, and Gato, a fifteen-year-old buckskin.</p>
	
			<p>Tschiffely has written an incident-packed account of equestrian, topographical, and anthropological interests in his Southern Cross to Pole Star (also called Tschiffely’s Ride). In it, he relates the many dangers and hardships he, Mancha & Gato endured throughout the 10,000 miles, as well as the joys and hospitality he received.</p>
	
			<p>Leaving the civilization of a capital city, the horses carried him through a wide variation of terrain and climate, as it could be possible to encounter. They did it alone, with no back-up-crews tailing them with creature comforts for man or horse; with little in the way of communications available once away from the well-used tracks and into almost unmapped wilderness that made up much of their route. 1926 South America saw many regions still unexplored, or relatively unknown. Though just over seventy years ago, it was a very different age, especially in the primitive areas of the South and Central American countries the trio traversed and which comprised the bulk of the epic journey.</p>
			
			<a rel="group" class="fb img right" title="Tshiffely with Mancha &amp; Gato" href="images/tmg.jpg"><img src="images/tmg-s.jpg" alt="tmg-s" width="150" height="99" /></a>
	
			<p>The horses used came originally from the Patagonian pampas. Of the renowned tough Criollo breed, Tschiffely had the faith in, and the wish to prove, Criollo supremacy in continuous work under harsh conditions. Bred almost wild, they retained an inborn sense of self-preservation and Tschiffely learnt to respect his horses' judgement of safety, especially after Gato saved them all from miring in quicksand and in dry regions they gave advance warning of water ahead.</p>
			
			<a rel="group" class="fb img left" title="Painting of Tshiffely with Mancha &amp; Gato by Ferdinando Tacconi" href="images/painting.jpg"><img src="images/painting-s.jpg" alt="tmg-s" width="150" height="155" /></a>
	
			<p>Being before the days of modern veterinary science, old time herbal remedies proved sovereign, and one item in Tschiffely's pharmacopoeia was garlic. Used crashed with Indian pepper it was reputed to keep the bats away, crushed with alcohol it was a remedy against mountain sickness; and by itself, again crushed, it was supposed to be so noxious to snakes that they would stay away. All three were hazards at differing stages of the journey and likely to bother men and horses.</p>

			<p>Throughout his tale, Tschiffely pays tribute to Mancha and Gato, their courage and sagacity; the sheer doggedness displayed in conditions more often than not adverse. A lot was asked of two chunky pampas horses and they gave in generous measure, proving beyond doubt all that is claimed for them by Criollo aficionados.</p>

			<p>In 1974 Gordon Roddick completed 2,000 miles of Tschiffely’s Ride and in 1989, James Greenwood went as far as Peru, Lima, finishing 4,000 miles of the trip.</p>
			
